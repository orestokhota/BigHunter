package com.magaz.dto;

public class TypeDto {
	
	private String typeName;

	public TypeDto() {
		super();
	}

	public TypeDto(String typeName) {
		super();
		this.typeName = typeName;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	
	
}
